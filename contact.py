class Contact():

    def __init__(self, name, last_name, nick_name, company_name, address, phone_number,email, b_day, b_month, b_year):
        self.name = name
        self.last_name = last_name
        self.nick_name = nick_name
        self.company_name = company_name
        self.address = address
        self.phone_number = phone_number
        self.email = email
        self.b_day = b_day
        self.b_month = b_month
        self.b_year = b_year
